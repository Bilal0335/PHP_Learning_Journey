<?php
// Normal assignment
$a = 10;        // $a = 10

// Add and assign
$a = 10;
$b = 5;
$a += $b;       // $a = $a + $b = 15
echo "Addition assignment: $a <br>";

// Subtract and assign
$a = 10;
$b = 5;
$a -= $b;       // $a = $a - $b = 5
echo "Subtraction assignment: $a <br>";

// Multiply and assign
$a = 10;
$b = 5;
$a *= $b;       // $a = $a * $b = 50
echo "Multiplication assignment: $a <br>";

// Divide and assign
$a = 10;
$b = 5;
$a /= $b;       // $a = $a / $b = 2
echo "Division assignment: $a <br>";

// Modulus and assign
$a = 10;
$b = 3;
$a %= $b;       // $a = $a % $b = 1
echo "Modulus assignment: $a <br>";

// Exponentiation and assign
$a = 2;
$b = 3;
$a **= $b;      // $a = $a ** $b = 2^3 = 8
echo "Exponent assignment: $a <br>";
?>